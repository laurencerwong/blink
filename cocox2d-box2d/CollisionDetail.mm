#import "CollisionDetail.h"

@implementation CollisionDetail

@synthesize pointOfCollision = pointOfCollision_;

-(void)SetProperties:(GamePhysicsObject*)type WithPoint:(CGPoint)pointOfCollision
{
    self.pointOfCollision = pointOfCollision;
    object_ = type;
}

-(GamePhysicsObject *)getObject
{
    return object_;
}

@end
